package org.openmrs.module.patientaccesscontrol;

import java.io.Serializable;

import org.openmrs.Patient;
import org.openmrs.Program;

public class PatientProgramModel implements Serializable {
	
	private static final long serialVersionUID = 1L;
	
	private Program program;
	
	private Patient patient;
	
	public PatientProgramModel(Patient patient, Program program) {
		this.patient = patient;
		this.program = program;
	}
	
	public Program getProgram() {
		return program;
	}
	
	public void setProgram(Program program) {
		this.program = program;
	}
	
	public Patient getPatient() {
		return patient;
	}
	
	public void setPatient(Patient patient) {
		this.patient = patient;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((patient == null) ? 0 : patient.hashCode());
		result = prime * result + ((program == null) ? 0 : program.hashCode());
		return result;
	}
	
	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		PatientProgramModel other = (PatientProgramModel) obj;
		if (patient == null) {
			if (other.patient != null) {
				return false;
			}
		} else if (!patient.equals(other.patient)) {
			return false;
		}
		if (program == null) {
			if (other.program != null) {
				return false;
			}
		} else if (!program.equals(other.program)) {
			return false;
		}
		return true;
	}
	
	@Override
	public String toString() {
		return patient.toString() + ", " + (program != null ? program.toString() : "Program(null)");
	}
	
}
